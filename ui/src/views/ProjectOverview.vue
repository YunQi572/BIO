  mounted() {
    this.initAnimations();
  },
  methods: {
    initAnimations() {
      // è§‚å¯Ÿå…ƒç´ è¿›å…¥è§†çª—
      const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            entry.target.classList.add('visible');
            // å¦‚æœæ˜¯è®¡æ•°å™¨å…ƒç´ ï¼Œå¯åŠ¨è®¡æ•°åŠ¨ç”»
            if (entry.target.classList.contains('counter-animation')) {
              this.startCounter(entry.target);
            }
          }
        });
      }, {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
      });

      // è§‚å¯Ÿæ‰€æœ‰åŠ¨ç”»å…ƒç´ 
      document.querySelectorAll('.fade-in-up, .slide-in-left, .slide-in-right, .counter-animation').forEach(el => {
        observer.observe(el);
      });
    },
    
    startCounter(element) {
      const target = parseInt(element.getAttribute('data-target'));
      const current = parseInt(element.querySelector('.stat-number').textContent.replace(/[^0-9]/g, ''));
      
      if (current >= target) return; // å·²ç»è®¡æ•°è¿‡äº†
      
      const duration = 2000;
      const increment = target / (duration / 16);
      let currentValue = 0;

      const timer = setInterval(() => {
        currentValue += increment;
        if (currentValue >= target) {
          currentValue = target;
          clearInterval(timer);
        }
        
        const displayValue = Math.floor(currentValue);
        if (target >= 1000) {
          element.querySelector('.stat-number').textContent = displayValue.toLocaleString() + '+';
        } else {
          element.querySelector('.stat-number').textContent = displayValue + '+';
        }
      }, 16);
    }
  }
}
